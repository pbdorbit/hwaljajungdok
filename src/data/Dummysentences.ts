// 랜덤으로 문장 하나 뽑기
export const getRandomSentence = (): DummySentence => {
  const index = Math.floor(Math.random() * DUMMY_SENTENCES.length);
  return DUMMY_SENTENCES[index];
};

// 중복 없이 N개 랜덤 뽑기 (컬렉션 초기 더미용)
export const getRandomSentences = (count: number): DummySentence[] => {
  const shuffled = [...DUMMY_SENTENCES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, DUMMY_SENTENCES.length));
};
